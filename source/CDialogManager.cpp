#include "CDialogManager.h"

///////////////////////////////////////////////////////////////////////////////////////////
// Constructor en Deconstructor
///////////////////////////////////////////////////////////////////////////////////////////
CDialogManager::CDialogManager()
{
}

CDialogManager::~CDialogManager()
{
}

///////////////////////////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
///////////////////////////////////////////////////////////////////////////////////////////
int CDialogManager::ShowDialog(int nDialog)
{
	switch (nDialog)
	{
	case About:
		return m_About.Show();
		break;

	case Plugin:
		return m_Plugin.Show();
		break;

	default:
		return 0;
	}
}

///////////////////////////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
///////////////////////////////////////////////////////////////////////////////////////////
void CDialogManager::InitDialogs(HWND hOwner, HINSTANCE hInstance)
{
	this->m_hOwner = hOwner;
	this->m_hInstance = hInstance;

	m_About.Init(m_hOwner, m_hInstance);
	m_Plugin.Init(m_hOwner, m_hInstance);
}